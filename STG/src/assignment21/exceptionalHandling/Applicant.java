package assignment21.exceptionalHandling;

public class Applicant {
    private String applicantName;
    private String postAppied;
    private int applicantAge;

    public String getApplicantName() {
        return applicantName;
    }

    public void setApplicantName(String applicantName) {
        this.applicantName = applicantName;
    }

    public String getPostAppied() {
        return postAppied;
    }

    public void setPostAppied(String postAppied) {
        this.postAppied = postAppied;
    }

    public int getApplicantAge() {
        return applicantAge;
    }

    public void setApplicantAge(int applicantAge) {
        this.applicantAge = applicantAge;
    }
}
